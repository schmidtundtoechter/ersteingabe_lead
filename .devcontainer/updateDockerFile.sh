#!/bin/bash

cd $(dirname $0)

cp -R ../../frappe_docker/development .
cp -R ../../frappe_docker/images/production/Containerfile .
cp -R ../../frappe_docker/resources .
cp -R ../../frappe_docker/devcontainer-example/* .
cp -R development/vscode-example development/.vscode

